func TestTamperedMessage(t *testing.T) {
	kg := NewKeyGenerator(kv2.NewBlakeSHA256Ed25519())
	message := string("abcdefg")
	tamperedM := string("ijklmnop")
	priv, pub := kg.GeneratePair()

	// Generate correct signature
	sig, err := priv.Sign([]byte(message), signer)
	logError(err)
	err = pub.Verify([]byte(tamperedM), sig, signer)
	logError(err)
	assert.NotNil(t, err, "could verify signature with tampered message")
}

func TestWrongPublicKey(t *testing.T) {
	kg := NewKeyGenerator(kv2.NewBlakeSHA256Ed25519())
	message := string("abcdefg")
	priv, _ := kg.GeneratePair()
	_, pub2 := kg.GeneratePair()
	// Change pubkey
	sig, err := priv.Sign([]byte(message), signer)
	logError(err)
	err = pub2.Verify([]byte(message), sig, signer)
	logError(err)
	assert.NotNil(t, err, "could verify signature with wrong public key")
}
